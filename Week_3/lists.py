# append () , insert () , remove () , pop () , index () , count () , sort () , reverse()

items = ["pen","ruler","laptop","mathematical set"]
print(items)

print("\n")

items.append("phone")
print(items)

print("\n")

items.pop()
print(items)

print("\n")

items.remove("ruler")
print(items)

print("\n")

items.reverse()
print(items)

print("\n")

items.sort()
print(items)

print("\n")

x = items.count("laptop")
print(x)

print("\n")

items.insert(0,"pencil")
print(items)

print("\n")
